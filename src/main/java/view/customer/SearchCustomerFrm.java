    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
            JTable source = (JTable)evt.getSource();
            int row = source.rowAtPoint( evt.getPoint() );
            int idKey = Integer.parseInt( source.getModel().getValueAt(row, 0)+"");
            CustomerDAO customerDao = new CustomerDAO();
            Customer customer = customerDao.getCustomerById(idKey);
            JOptionPane.showMessageDialog(null,"bạn chọn khách hàng có id= "+ idKey);
            
          
            BorrowCard borrowCard = new BorrowCard();
            int maxID = new BorrowCardDAO().getMaxCurrentId();
            borrowCard.setId(maxID+1);
            borrowCard.setUser(this.user);
            borrowCard.setCustomer(customer);
            AddClothesRentFrm myFrm = new AddClothesRentFrm(borrowCard, null);
            myFrm.setVisible(true);
    }//GEN-LAST:event_jTable1MouseClicked

