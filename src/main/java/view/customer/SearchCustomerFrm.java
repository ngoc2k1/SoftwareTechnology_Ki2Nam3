    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        CustomerDAO customerDao = new CustomerDAO();
        String key = jTextField1.getText();
        ArrayList<Customer> listCustomer = customerDao.searchCustomer(key);
        String data[][] = new String[listCustomer.size()][7];
        for(int i = 0; i < listCustomer.size(); i++){
            Customer customer = listCustomer.get(i);
            data[i][0] = customer.getId()+"";
            data[i][1] = customer.getName();
            data[i][2] = customer.getIdCard();
            data[i][3] = customer.getAddress();
            data[i][4] = customer.getTel();
            data[i][5] = customer.getEmail();
            data[i][6] = customer.getNote();
        }
        System.out.println(listCustomer);
        String column[] = {"Id", "Name", "IdCard", "Address", "Tel", "Email", "Note"};
        DefaultTableModel modelTable = new DefaultTableModel(data, column);
        this.jTable1.setModel(modelTable);
    }//GEN-LAST:event_jButton1ActionPerformed

