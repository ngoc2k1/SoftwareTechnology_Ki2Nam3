    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //            get day 
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        try {
            Date daypay = sdf.parse(jTextField1.getText() );
            Date dayrent = sdf.parse(jTextField2.getText() );
            
            this.borrowCard.setDaypay(daypay);
            this.borrowCard.setDayrent(dayrent);
            
            this.borrowCard.calcTotalcost(dayrent, daypay, this.borrowCard.getListCR());
            this.borrowCard.calcDeposit(this.borrowCard.getListCR());
        } catch (ParseException ex) {
            Logger.getLogger(AddBorrowCardFrm.class.getName()).log(Level.SEVERE, null, ex);
        }
        float rentCR = 0;
        for(ClothesRent cr: borrowCard.getListCR()){
            rentCR += cr.getTotal();
        }
        int daydiff = Integer.parseInt((borrowCard.getTotalcost() / rentCR) + "");
        jLabel7.setText("Tổng số tiền thuê: "+rentCR+"đ × "+daydiff+" = ");
        jLabel10.setText(borrowCard.getTotalcost()+"");
        jLabel8.setText("Tổng số tiền đặt cọc: ");
        jLabel11.setText(borrowCard.getDeposit()+"");
        jLabel9.setText("Khách hàng đặt cọc thêm: "+ jTextField3.getText());
        jTextField3.setBounds(0, 0, 0, 0);
    }//GEN-LAST:event_jButton1ActionPerformed

